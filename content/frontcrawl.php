      <h3>High Elbow</h3>
      <p>A good front crawl will have elbows higher than rest of the arm during each recovery phase. A good way to picture this is to have your shoulder, elbow and hand as 3 points of a triangle. The bottom two corners of the triangle touching the water should be your shoulder and your hand while the elbow rests high above the water. Here's a list of variations of high elbow drill.</p>
      <ul>
