      <h3>Front Glide & Side Glide</h3>
      <p>Many times students go straight to front crawl and do not build strong fundamentals. Front glide and side glide combination swim is one of the most important basics to a good front crawl. TO practic: Start with two hands holding the end of a kick board and student swimming front glide. When the student needs to breath, roll onto his or her side and hold the kick board with one arm and perform a side glide. Once the student has taken a breath return to front glide position with 2 hands on kick board. Repeat this drill until student is strong enough to hold side glide without a kick board.</p>
