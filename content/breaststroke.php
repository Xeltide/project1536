      <h3>Double kick and glide</h3>
      <p>This drill focuses on developing a good coordination and glide phase for breast stroke. To start have student to breast stroke with a ratio of two kicks to one breath. Instead of coming up every time for air, student will kick and glide twice before breathing. Challenge students to glide longer and cross the distance of the pool with least amount of kicks possible. The first kick after each breath should push students deeper than the normal breast stroke kick.</p>
    </div>
    <div class="five columns">
      <h3>Noodle arms</h3>
      <p>This drill is to correct for over pulling of arms during breathing phase of breaststroke. Have each student grab a hard pool noodle and place it under both their arm pits. Students will swim breast stroke in this position to prevent them from pulling under their body and past their hips. The arms for breast stroke should give just enough lift for a swimmer to breath and the legs should be doing the pushing.</p>
