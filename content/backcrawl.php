      <h3>Three Second Pause</h3>
      <p>This drill works on strong kicks and control of arm movement. Student will pause their arms each time they alternate to head and hip position. Student should count slow to three while continuously kicking. Once the three seconds have pasts alternate arms again. Focus on correct arm recovery movement and develop strong kicks.</p>
      <p>For students needing more of a challenge, instead of holding pause at head and hip position, have students hold pause infront of their body out of the water.</p>
