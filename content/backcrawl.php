      <h3>Gates</h3>
      <p>This drill will make sure students arm movements are inline with their bodies. To start partner up in groups of 2s. Have one student hold out there hands with thumbs pointing inwards and rest of the fingers pointing forwards and aligned vertically infront of their body. The second student will practice rolling their shoulders and their backcrawl arm strokes by looking at the thumbs of the first students and making sure his or her ahnds travel through the gate the first student has made. Make sure students stand about arms length facing each other. Once the students have gotten the hang of this move onto the practice positioning for backcrawl strokes behind the student. Now have the student making the gate stand arms length behind the student practicing. The gate student will hold out his or her hands with palms facing up about arms length away behind and just below the should blades of the first student. The first student will have to try to high five the second students hands each time they practice their backcrawl arms while keeping their head and body straight and facing forward.</p>
